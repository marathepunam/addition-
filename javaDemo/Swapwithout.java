package javaDemo;

public class Swapwithout 
{

	public static void main(String[] args) 
	{
		int a=5;
		int b=10;
		int temp;
		System.out.println("Before swapping: a= " + a +" , b = " + b);
		temp=b; 
		b=a;
		a=temp;
		System.out.println("After Swapping: a= " + a +" , b = "+b);
	}			
				
}				
				